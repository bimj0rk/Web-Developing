function openNav(){
    document.getElementById('sidenav').style.width = "300px";
    document.getElementById('sidenav').style.boxShadow = "0 0 0 10000px rgba(23, 37, 42, .75)";
}

function closeNav(){
    document.getElementById('sidenav').style.width = "0px";
    document.getElementById('sidenav').style.boxShadow = "0 0 0 10000px rgba(58, 175, 169, 0)";
}
